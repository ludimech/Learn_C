#include <stdio.h>

int main()
{
    int num1 = 2;
    int num2 = 4;

    printf("Where num1 = 2 and num2 = 4, we get the following:");

    printf("\nWhen subtracting num1 from num2 we get: %d", num2 - num1);
    printf("\nWhen adding num1 to num2 we get: %d", num2 + num1);
    printf("\nWhen multiplying num1 and num2 we get: %d", num2 * num1);
    printf("\nWhen dividing num2 by num1 we get: %d", num2 / num1);
    
    return 0;
}